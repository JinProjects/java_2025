package com.company.java005_ex;

import java.util.Scanner;

public class Repeat002 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.print("숫자 한개 입력해주세요 >");
		int num = sc.nextInt();
		
		switch(num) {
		case 10:
		System.out.println("10이다");
		break;
		case 20:
			System.out.println("10이다");
		break;
		case 30:
			System.out.println("10이다");
		break;
		}
	}
}
