package jsp_basic;

public class Servlet001 {
	private int a;
	private int b;
	
	public Servlet001() {
		super();
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public Servlet001(int a, int b) {
		super();
		this.a = a;
		this.b = b;
	}
	
	
}
